// get a wrong answer
// * correct_answer:kanji(japanese)
// * question:hiragana
function getWrongAnswer_Writing(correct_answer, question) {
    // First, try to create "CREATION TABLE" (= the replace target and replace function) by kanji info
    // Because not all kanji have enough info to replace the word, we must create rules according to each target kanji
    var kanji_wrong_answer_creation_table = [];

    // the replace target kanji array (1 index for 1 kanji & random order)
    var targetKanjiArray = arrayShuffle(correct_answer.split('')).join('').match(REGEXP_KANJI);
    if (! targetKanjiArray) {
        // trim hiragana from kanji(=question) 
        var change_target_org = trimHiragana(correct_answer, question)['word1'];
        var targetKatakanaArray = change_target_org.match(REGEXP_KATAKANA);
        if(! targetKatakanaArray){
            // no kanji & katakana
            return correct_answer;
        }

        // if "correct_answer" param has no kanji & has 1 or more katakana,
        // this will be a katakana writing question (create a katakana wrong answer)
        return getWrongAnswer_Writing_Katakana(correct_answer, question);
    }

    $.each(targetKanjiArray, function(index, target){
        var kanjiInfo = KANJI_INFO_MAP[target];
        if (! kanjiInfo){
            // if there is no info for that kanji, skip it
            return;
        }

        // if the target kanji has similar kanji, add the replacer for that (maximum 5 = 50%)
        var similar = arrayFixLen(arrayShuffle(kanjiInfo.similar), 5);
        for(var i=0; i<similar.length; i++){
            kanji_wrong_answer_creation_table.push({
                "patterns": [target], // target kanji
                "func": function(){
                    return similar[i];
                } // replace function
            });
        }

        // add the replacer for reading (maximum 2 = 20%)
        var reading = arrayFixLen(arrayShuffle(kanjiInfo.reading), 2);
        for(var i=0; i<reading.length; i++){
            kanji_wrong_answer_creation_table.push({
                "patterns": [target], // target kanji
                "func": function(){
                    var res = getOneKanjiFromReading(reading[i]);
                    return res ? res : target;
                } // replace function
            });
        }

        // if the target kanji has bushu info, add the replacer for that (maximum 2 = 20%)
        var bushu = arrayFixLen(arrayShuffle(kanjiInfo.bushu), 2);
        for(var i=0; i<bushu.length; i++){
            kanji_wrong_answer_creation_table.push({
                "patterns": [target], // target kanji
                "func": function(){
                    var res = getOneKanjiFromBushu(bushu[i])
                    return res ? res : target;
                } // replace function
            });
        }
        
        // random choice replacer from same grade kanji
        kanji_wrong_answer_creation_table.push({
            "patterns": [target], // target kanji
            "func": function(){
                return arrayShuffle(ALL_KANJI_BY_GRADE[kanjiInfo.grade - 1])[0]['key'];
            } // replace function
        });
    });
    
    if(kanji_wrong_answer_creation_table.length == 0){
        // no replacer added, add random choice replacer from all kanji
        kanji_wrong_answer_creation_table.push({
            "patterns": [targetKanjiArray[0]], // target kanji
            "func": function(){
                return arrayShuffle(Object.keys(KANJI_INFO_MAP))[0];
            } // replace function
        });
    }

    // create "CREATION TABLE" finished
    // try to make a wrong answer by the rule of "CREATION TABLE"
    var rep_objs = arrayShuffle(kanji_wrong_answer_creation_table);
    for (var i = 0; i < rep_objs.length; i++) {
        var change_target = getWrongAnswerCore(correct_answer, rep_objs[i]["patterns"], rep_objs[i]["func"]);
        if(! change_target){
            // get failed, try next rule
            continue;
        }

        return change_target
    }
}

function getOneKanjiFromReading(reading){
    var ret = null;
    // find same reading from all kanji
    $.each(arrayShuffle(Object.keys(KANJI_INFO_MAP)), function(index, kanji){
        if(KANJI_INFO_MAP[kanji].reading.indexOf(reading) >= 0){
            // return the kanji that has same reading
            ret = kanji;
            return false;
        }
    });

    return ret;
}

function getOneKanjiFromBushu(bushu){
    var ret = null;
    // find same bushu from all kanji
    $.each(arrayShuffle(Object.keys(KANJI_INFO_MAP)), function(index, kanji){
        if(KANJI_INFO_MAP[kanji].bushu.indexOf(bushu) >= 0){
            // return the kanji that has same bushu
            ret = kanji;
            return false;
        }
    });

    return ret;
}



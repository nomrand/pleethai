// * correct_answer:katakana(japanese)
// * question:hiragana
function getWrongAnswer_Writing_Katakana(correct_answer, question) {
    // try to make a wrong answer by the rule of "CREATION TABLE"
    var rep_objs = arrayShuffle(KATAKANA_WRONG_ANSWER_CREATION_TABLE);
    for (var i = 0; i < rep_objs.length; i++) {
        var change_target = getWrongAnswerCore(correct_answer, rep_objs[i]["patterns"], rep_objs[i]["func"]);
        if(! change_target){
            // get failed, try next rule
            continue;
        }

        // get succeed
        var wrong_answer = correct_answer.replace(correct_answer, change_target);
        // max 50% chance to do it again
        var doAgainRatio = 0.5;
        if(change_target.length <= 5){doAgainRatio = 0.3;}
        if(change_target.length <= 3){doAgainRatio = 0.1;}
        if(Math.random() < doAgainRatio){
            wrong_answer = getWrongAnswer_Writing_Katakana(wrong_answer, question)
        }
        return wrong_answer;
    }

    // get failed completely
    return undefined;
}
